package org.dlsu.arrowsmith.models;

public class Degreeprogram {
	private String degreeprogramId;
	private String collegeId;
	private String deptId;
	private String degreeprogramCode;
	private String degreeprogramName;

	public String getDegreeprogramId() {
		return degreeprogramId;
	}

	public void setDegreeprogramId(String degreeprogramId) {
		this.degreeprogramId = degreeprogramId;
	}

	public String getCollegeId() {
		return collegeId;
	}

	public void setCollegeId(String collegeId) {
		this.collegeId = collegeId;
	}

	public String getDeptId() {
		return deptId;
	}

	public void setDeptId(String deptId) {
		this.deptId = deptId;
	}

	public String getDegreeprogramCode() {
		return degreeprogramCode;
	}

	public void setDegreeprogramCode(String degreeprogramCode) {
		this.degreeprogramCode = degreeprogramCode;
	}

	public String getDegreeprogramName() {
		return degreeprogramName;
	}

	public void setDegreeprogramName(String degreeprogramName) {
		this.degreeprogramName = degreeprogramName;
	}
}
