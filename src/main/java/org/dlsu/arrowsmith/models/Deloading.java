package org.dlsu.arrowsmith.models;

public class Deloading {
	
	private String deloadingId;
	private String collegeId;
	private String deptId;
	private String deloadingCode;
	private String deloadingName;
	private String deloadingType;
	private String deloadUnits;
	private String description;
	
	public String getDeloadingId() {
		return deloadingId;
	}
	public void setDeloadingId(String deloadingId) {
		this.deloadingId = deloadingId;
	}
	public String getCollegeId() {
		return collegeId;
	}
	public void setCollegeId(String collegeId) {
		this.collegeId = collegeId;
	}
	public String getDeptId() {
		return deptId;
	}
	public void setDeptId(String deptId) {
		this.deptId = deptId;
	}
	public String getDeloadingCode() {
		return deloadingCode;
	}
	public void setDeloadingCode(String deloadingCode) {
		this.deloadingCode = deloadingCode;
	}
	public String getDeloadingName() {
		return deloadingName;
	}
	public void setDeloadingName(String deloadingName) {
		this.deloadingName = deloadingName;
	}
	public String getDeloadingType() {
		return deloadingType;
	}
	public void setDeloadingType(String deloadingType) {
		this.deloadingType = deloadingType;
	}
	public String getDeloadUnits() {
		return deloadUnits;
	}
	public void setDeloadUnits(String deloadUnits) {
		this.deloadUnits = deloadUnits;
	}
	public String getDescription() {
		return description;
	}
	public void setDescription(String description) {
		this.description = description;
	}
	
	

}
